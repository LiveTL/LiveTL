<script>
  import { channelFilters, enableMchadTLs, mchadUsers } from '../../js/store.js';
  import MultiDropdown from '../options/MultiDropdown.svelte';
  import Card from '../../submodules/chat/src/components/common/Card.svelte';

  export let boundingDiv;

  let width = 0;
</script>

<Card title="Blocked Users" icon="block">
  <div class="flex gap-2 flex-wrap" bind:clientWidth={width}>
    <div class="flex-1">
      <MultiDropdown
        name="Chat users"
        store={channelFilters}
        getDisplayName={(n, v) => v.name}
        getBool={n => channelFilters.get(n).blacklist}
        setBool={(n, v) =>
          channelFilters.set(n, { ...channelFilters.get(n), blacklist: v })}
        {boundingDiv}
        {width}
      />
    </div>
    {#if $enableMchadTLs}
      <div class="flex-1">
        <MultiDropdown
          name="MChad"
          store={mchadUsers}
          {boundingDiv}
          {width}
        />
      </div>
    {/if}
  </div>
</Card>
